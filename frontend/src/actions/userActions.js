export const getUserDetails = (id) => async (dispatch, getState) => {
  try {
    dispatch({
      type: USER_DETAILS_REQUEST,
    });

    // console.log(getState());

    /*
    These two lines are equal to the object destructuring below 
    const { userLoginReducer } = getState();
    const { userInfo } = userLoginReducer;
    */
    const {
      userLoginReducer: { userInfo },
    } = getState();

    // We have to include a bearer token to get our user as we do in the postman header
    const config = {
      headers: {
        "Content-type": "application/json",
        Authorization: `Bearer ${userInfo.token}`,
      },
    };

    const { data } = await axios.get(`/api/users/${id}/`, config); //id here is equal to 'profile', and we have to pass the token to get the user data

    dispatch({
      type: USER_DETAILS_SUCCESS,
      payload: data,
    });
  } catch (error) {
    dispatch({
      type: USER_DETAILS_FAIL,
      payload:
        error.response && error.response.data.detail
          ? error.response.data.detail
          : error.message,
    });
  }
};

